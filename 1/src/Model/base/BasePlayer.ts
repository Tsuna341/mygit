import { E_GENDER, E_REALM, E_REALM_STAGE, E_MENPAI, E_LINGGEN } from "../consts/PlayerEnum";

export interface P_SAVE
{
    name: string,
    gender: number,
    age: number,
    hp: number,
    mp: number,
}

// 人物基类
export default abstract class BasePlayer extends Laya.Script
{
    /////////////////////////////////////////////////////////////////////////////////
    /// 人物基础值
    /////////////////////////////////////////////////////////////////////////////////
    // 唯一标识符
    public ID: number = 0;
    // 名字
    public name: string = "";
    // 性别
    public gender: E_GENDER = E_GENDER.MAN;
    // 年龄
    public age: number = 0;
    // hp
    public hp: number = 0;
    // 法力值
    public mp: number = 0;
    // 灵根
    public lingGens: Array<E_LINGGEN> = [];

    /////////////////////////////////////////////////////////////////////////////////
    /// 境界相关
    /////////////////////////////////////////////////////////////////////////////////

    // 境界
    public realm: E_REALM = E_REALM.R_FANREN;
    // 阶段
    public realmStage: E_REALM_STAGE = E_REALM_STAGE.LOW;

    /////////////////////////////////////////////////////////////////////////////////
    /// 所属门派
    /////////////////////////////////////////////////////////////////////////////////

    // 门派
    public menPai: E_MENPAI = E_MENPAI.NULL;

    /////////////////////////////////////////////////////////////////////////////////
    /// 抽象方法
    /////////////////////////////////////////////////////////////////////////////////

    // 存储存档
    public abstract save(): void;
    // 读取存档
    public abstract loadSave(): void;
}